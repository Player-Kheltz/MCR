-- Quest: Aventura
NOME: Aventura na Floresta Encantada

DESCRICAO: Você, o valente explorador, recebeu uma missão de um sacerdote local para encontrar e coletar os frutos sagrados da floresta encantada antes que o temido dragão venenoso possa atacar. A floresta é conhecida por suas criaturas mágicas e seus segredos ocultos.

OBJETIVOS:
- Explorar a Floresta Encantada
- Coletar 10 frutos sagrados (ID: 102)
- Encontrar a Pedra Esmeralda (ID: 201)

RECOMPENSA:
xp: 500
itens: ID:QTD
