local os = require("os")

print("Hora atual: " .. os.date("%H:%M"))
```

Este script Lua usa a função `os.date()` para obter a hora atual e a formata com `%H:%M`, que representa as horas e minutos no formato de 24h. Em seguida, ele imprime a hora na tela.